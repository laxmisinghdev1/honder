const translations: Record<SupportedLanguage, TranslationMap> = {
  es: esTranslations,
  en: enTranslations,
  pt: ptTranslations,
  zh: zhTranslations,
}

type LanguageContextValue = {
  language: SupportedLanguage
  languages: readonly SupportedLanguage[]
  setLanguage: (language: SupportedLanguage) => void
  t: (key: string) => string
}

const LanguageContext = createContext<LanguageContextValue | null>(null)

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<SupportedLanguage>(() => {
    if (typeof window === 'undefined') {
      return 'es'
    }

    const stored = window.localStorage.getItem('hondar-language') as SupportedLanguage | null
    return stored && SUPPORTED_LANGUAGES.includes(stored) ? stored : 'es'
  })

  const setLanguage = useCallback((nextLanguage: SupportedLanguage) => {
    setLanguageState(nextLanguage)
    window.localStorage.setItem('hondar-language', nextLanguage)
  }, [])

  useEffect(() => {
    document.documentElement.lang = language
  }, [language])

  const t = useCallback(
    (key: string) => {
      return translations[language][key] ?? translations.es[key] ?? key
    },
    [language],
  )

  const value = useMemo(
    () => ({
      language,
      languages: SUPPORTED_LANGUAGES,
      setLanguage,
      t,
    }),
    [language, setLanguage, t],
  )

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within LanguageProvider')
  }

  return context
}
